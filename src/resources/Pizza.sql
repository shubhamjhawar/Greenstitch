USE hondb;

DROP TABLE if exists PIZZA CASCADE;
DROP TABLE if exists PIZZA_ORDERS CASCADE;

CREATE TABLE PIZZA(
 PIZZAID Integer NOT NULL,
 PIZZANAME VARCHAR(250),
 PRICE DOUBLE,
 CONSTRAINT XYZ_PIZZA_PK PRIMARY KEY(PIZZAID)
);


CREATE TABLE PIZZA_ORDER(
  ORDERID Integer AUTO_INCREMENT,
  CUSTOMERNAME VARCHAR(20) NOT NULL,
  CONTACTNUMBER VARCHAR(10) NOT NULL,
  PIZZAID INTEGER,
  NUMBEROFPIECESORDERED INTEGER,
  BILL DOUBLE ,
  CONSTRAINT PIZZA_ORDER_PK PRIMARY KEY (ORDERID),
  CONSTRAINT PIZZA_ORDER_FK FOREIGN KEY(PIZZAID) REFERENCES PIZZA(PIZZAID)
);



